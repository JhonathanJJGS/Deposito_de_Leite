void draw_screen(){
   u8g2.setFont(u8g2_font_t0_18_mr);
    //Motor ON
    u8g2.drawFrame(3,3,120,30);
    u8g2.drawVLine(64,4,28);
    u8g2.drawStr(8,23,"M: ");
    //u8g2.setCursor(30, 23);
    //u8g2.print("OFF");
    u8g2.setFont(u8g2_font_unifont_t_symbols);
    u8g2.drawGlyph(35, 23, 0x2715);
    //RESFRIADOR
    u8g2.drawStr(67,23,"RE: ");
    //u8g2.setCursor(92, 23);
    //u8g2.print("OFF");
    u8g2.setFont(u8g2_font_unifont_t_symbols);
    u8g2.drawGlyph(97, 23, 0x2714);
    //TEMPERATURA
    u8g2.drawFrame(3,34,120,30);
    u8g2.drawStr(8,55,"TEMP: ");
    u8g2.setCursor(59, 55);
    u8g2.print(getTemperaturaC());
    u8g2.drawCircle(115, 42, 2, U8G2_DRAW_ALL);
    u8g2.drawStr( 104, 55, "C");

}

void draw_page(){
       u8g2.firstPage();
  do {
    if(display_boot == 0){
       draw_logo();
    }else if (display_boot == 1){
       draw_screen();
    }
  } while ( u8g2.nextPage() );
   delay(2000);
   display_boot = 1;
}